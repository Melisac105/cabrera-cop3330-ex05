package util;

public class BasicOperations {
    static public int calcSum(int in1, int in2) {
        return  in1 + in2;
    }

    static public int calcDifference(int in1, int in2) {
        return  in1 - in2;
    }

    static public int calcProduct(int in1, int in2) {
        return  in1 * in2;
    }

    static public int calcDivision(int in1, int in2) {
        return  in1 / in2;
    }

}
