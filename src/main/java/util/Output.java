package util;

public class Output {

    static public void printOutput(int in1, int in2, int sum, int diff, int product, int division) {
        System.out.format("%d + %d = %d \n%d - %d = %d \n%d * %d = %d \n%d / %d = %d \n",in1, in2, sum, in1, in2, diff, in1, in2, product, in1, in2, division);
    }
}
